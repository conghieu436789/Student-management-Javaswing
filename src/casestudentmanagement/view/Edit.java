    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed
        String ID = txtID.getText();
        String lastName = txtLastName.getText();
        String surName = txtSurName.getText();
        String address = txtAddress.getText();
        String email = txtEmail.getText();
        String BDString = txtBD.getText();
        Date birthDay = null;
        String gradeString = txtGrade.getText();
        double grade = 0d;
        try {
            birthDay = new SimpleDateFormat("dd/MM/yyyy").parse(BDString);
        } catch (ParseException ex) {
            lbBD.setForeground(Color.red);            
            JOptionPane.showMessageDialog(rootPane, "This format date is wrong");
            return;
        }
        try {
            grade = Double.parseDouble(gradeString);
        } catch (Exception ex) {
            lbGrade.setForeground(Color.red);
            JOptionPane.showMessageDialog(rootPane, "Grade must be a a number");
            return;
        }
        if (ID.equals("") || lastName.equals("") || surName.equals("") || address.equals("") ||
                email.equals("") || BDString.equals("") || gradeString .equals("")) {
            if (ID.equals("")) {
                lbID.setForeground(Color.red);
            }
            if (lastName.equals("")) {
                lbLastName.setForeground(Color.red);
            }
            if (surName.equals("")) {
                lbSurName.setForeground(Color.red);
            }
            if (address.equals("")) {
                lbAddress.setForeground(Color.red);
            }
            if (email.equals("")) {
                lbEmail.setForeground(Color.red);
            }
            if (BDString.equals("")) {
                lbBD.setForeground(Color.red);
            }
            if (gradeString.equals("")) {
                lbGrade.setForeground(Color.red);
            }
            JOptionPane.showMessageDialog(rootPane, "Please complete form");
        } else {
            Student student = new Student(ID, lastName, 
                surName, birthDay,email, address, grade);
        
            main.editStudent(student);
            JOptionPane.showMessageDialog(rootPane, "Sucessfully");
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_btSaveActionPerformed

